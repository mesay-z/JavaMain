package javaPractice;

public class DoWhileLoop {

    //Do-while loops are executed once automatically, while while loops check if the condition is true
// before excecuting the command.
    public static void main(String[] args) {
        int a =0;
        do {
            System.out.println(a+" i hate java");
            a++;
        }
        while(a<5);
    }
}
